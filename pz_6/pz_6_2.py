#Дан целочисленный список A размера N (< 15). Переписать в новый целочисленный
#список B все элементы с порядковыми номерами, кратными трем (3, 6, ...), и вывести
#размер полученного списка B и его содержимое. Условный оператор не
#использовать.


import random

def generate_random_list(n):
    """Генерирует случайный список длиной n, содержащий целые числа в диапазоне от -50 до 50."""
    return [random.randint(-50, 50) for _ in range(n)]

def extract_every_third_element(lst):
    """Извлекает элементы из списка lst, стоящие на позициях, кратных 3 (то есть 3-й, 6-й, 9-й и т.д.)."""
    return lst[2::3]

try:
    N = int(input("Введите длину списка N (от 1 до 14): "))  #Запрос длины списка у пользователя
    if N < 1 or N > 14:
        raise ValueError("Длина списка должна быть в диапазоне от 1 до 14")  #Проверка корректности введенного значения

    A = generate_random_list(N)  #Генерация случайного списка длиной N
    print("Исходный список A:", A)  #Вывод исходного списка

    B = extract_every_third_element(A)  #Извлечение элементов, стоящих на позициях, кратных 3

    print("Размер списка B:", len(B))  #Вывод размера результирующего списка
    print("Элементы списка B:", B)  #Вывод элементов результирующего списка

except ValueError as e:
    print(f"Произошла ошибка: {e}")  #Обработка возможной ошибки ввода
